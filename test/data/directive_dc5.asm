; .dc with multiple argument and specified size
.dc.a 1, 2, 3, 4    ; Address-sized arguments, based on size of address on target system
.dc.b 1, 2, 3, 4    ; 8-bit (byte) arguments
.dc.d 1, 2, 3, 4    ; Double-precision float arguments
.dc.l 1, 2, 3, 4    ; 32-bit (4-byte) arguments
.dc.s 1, 2, 3, 4    ; Single-precision float arguments
.dc.w 1, 2, 3, 4    ; 16-bit (2-byte) arguments
.dc.x 1, 2, 3, 4    ; Long double-precision float arguments
